    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (!campoDescripcion.getText().isEmpty()) {
            cambiarColorNegro();
            try {
                campoDescripcion.setEnabled(false);
                controller.guardar(new CategoriaDeBien(campoDescripcion.getText()));
                limpiarCampos();
                actualizarTabla();
            } catch (GlobalException | NoDataException | SQLException ex) {
                Logger.getLogger(ViewCatalogoBienes.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            cambiarColorRojo();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2 && evt.getButton() == MouseEvent.BUTTON1) {
            if (evt.getButton() == MouseEvent.BUTTON1) {
                int row = this.jTable1.getSelectedRow();
                aEliminar = controller.devolverCategoria(row);
                campoDescripcion.setText(aEliminar.getDescripcionCategoria());
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

